export class Person {
    public id: number;
    public name: string;
    public age: number;
    public gender: string;
    public weight: number;
    public height: number;

    constructor() {
        this.id = 0;
        this.name = '';
        this.age = 0;
        this.gender = '';
        this.weight = 0.0;
        this.height = 0.0;
    }
}
